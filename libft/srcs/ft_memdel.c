#include <stdlib.h>
#include <libft.h>

void	ft_memdel(void **ap)
{
	int		i;

	if (ap)
	{
		i = 0;
		while (ap[i])
		{
			free(ap[i]);
			i++;
		}
		*ap = NULL;
	}
}
